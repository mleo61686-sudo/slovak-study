/**
 * Verbs present phrases
 * Uses the same data + helpers as the frontend page.
 */
export function collectVerbsPresentItems(course: CourseId = "sk"): Item[] {
  const isCzech = course === "cs";
  const verbs = isCzech ? VERBS_CS : VERBS_SK;

  const collected = new Set<string>();

  for (const verb of verbs) {
    // Section 3: conjugation rows
    for (const row of verb.rows) {
      const full = row.full.trim();
      if (full) collected.add(full);
    }

    // Section 4: exact examples generated by the same helper as UI
    const examples = genExamplesFromRows(verb, isCzech);

    for (const ex of examples) {
      const base = ex.sk.trim();
      if (!base) continue;

      collected.add(base);
      collected.add(negateSentence(base, isCzech));
      collected.add(makeQuestion(base));
    }

    // Also include explicit examples from data file, if present
    for (const ex of verb.examples ?? []) {
      const base = ex.sk.trim();
      if (!base) continue;

      collected.add(base);
      collected.add(negateSentence(base, isCzech));
      collected.add(makeQuestion(base));
    }
  }

  const unique = Array.from(collected).map((p) => p.trim()).filter(Boolean);

  const base = unique.map((text) => ({
    kind: "phrase" as const,
    text,
  }));

  return expandDerivedPhrases(base);
}